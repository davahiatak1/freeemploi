<p class=MsoNormal style='text-align:justify'><span style='font-size:16.0pt;
line-height:107%'>                     <b>Questions fréquentes des entreprises</b></span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>1. <b>L’anonymat de nos utilisateurs (entreprises prêteuses,
entreprises hôtes et salariés) est-il préservé ?</b></span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Oui, l’anonymat de nos utilisateurs est une de nos
préoccupations majeures.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Ainsi, les pro&#64257;ls de salariés mis en ligne sont
anonymes et ne mentionnent ni le nom du salarié concerné ni celui de son
employeur (sauf si ce dernier en exprime la demande).</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Par ailleurs, lors des échanges réalisés via la messagerie
interne, les interlocuteurs sont identi&#64257;és uniquement par leur numéro
d’abonné et en aucun cas par leur nom/raison sociale.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Le nom de l’entreprise hôte est communiqué uniquement lorsque
celle-ci adresse sur le site une offre de mission à une entreprise prêteuse. Le
nom de l’entreprise prêteuse et celui de son salarié ne sont communiqués quant
à eux que lorsque l’entreprise prêteuse accepte une offre de mission.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Bien évidemment, nos utilisateurs sont libres d’échanger leur
identité quand ils le souhaitent via la messagerie interne.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>2<b>. Quelles sont les déclarations à effectuer dans le cadre
d’un prêt de main-d’œuvre ?</b></span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Le prêt de main-d’œuvre doit simplement être formalisé par
une convention de mise à disposition entre l’entreprise prêteuse et
l’entreprise hôte et par un avenant au contrat de travail du salarié concerné.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Si deux entreprises se prêtent plusieurs salariés
simultanément, chaque prêt de salarié doit donner lieu à la signature d’une
convention et d’un avenant.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Il n’y a pas lieu d’effectuer une déclaration à l’URSSAF, ni
à l’Inspection du travail.</span></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>3. Quelle communication doit être faite aux instances
représentatives du personnel lors d’un prêt de salarié ?</span></b></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Selon la loi, le comité d’entreprise ou, à défaut, les
délégués du personnel de l’entreprise prêteuse sont consultés préalablement à
la mise en œuvre du prêt et informés des différentes conventions signées. Le
comité d’hygiène, de sécurité et des conditions de travail (CHSCT) est
également informé lorsque le poste occupé dans l’entreprise hôte par le salarié
mis à disposition &#64257;gure sur la liste de ceux présentant des risques
particuliers pour la santé ou la sécurité des salariés (mentionnée au second
alinéa de l’article L.4154-2 du Code du travail).</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Le comité d’entreprise et le CHSCT ou, à défaut, les délégués
du personnel de l’entreprise hôte sont informés et consultés préalablement à
l’accueil de salariés mis à la disposition de celle-ci dans le cadre de prêts
de main-d’œuvre.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>En pratique, une consultation cadre des instances
représentatives du personnel suivie d’une information en &#64257;n d’année sur
les différentes conventions signées peuvent être réalisées.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Les entreprises dont l’effectif est inférieur à 11 salariés
et qui sont donc dépourvues d’instance représentative du personnel, ne sont,
par dé&#64257;nition, pas visées par cette obligation de communication.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>4<b>. Une entreprise peut-elle réaliser une opération de prêt
de main-d’œuvre avec une entreprise qui relève d’une convention collective
différente ?</b></span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Oui, deux entreprises ne relevant pas de la même convention
collective peuvent tout à fait avoir recours au prêt de salariés entre elles.
La convention collective applicable au salarié prêté demeurera celle en vigueur
au sein de son employeur, l’entreprise prêteuse.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>En conséquence, le salarié conserve, pendant la période de
mise à disposition, le béné&#64257;ce de l’ensemble des avantages de ladite
convention collective et plus généralement de l’ensemble des avantages
applicables au sein de l’entreprise prêteuse.</span></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>5. La durée du prêt doit-elle obéir à certaines règles ?</span></b></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Aucune disposition légale ne limite la durée de la mise à
disposition mais elle doit être déterminée. D’une journée à plusieurs mois, le
prêt peut être réalisé pour tout ou partie seulement du temps de travail du
salarié.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Dans l’hypothèse où le prêt est réalisé pour une partie
seulement du temps de travail du salarié, ce dernier retourne en principe chez
son employeur le reste du temps. Seul ce schéma est pris en compte dans les
modèles d’avenant et de convention proposés par Free-emploi.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Les modèles de convention et d’avenant disponibles sur
Free-emploi  ne sont pas adaptés aux prêts à l’occasion desquels le salarié ne
travaille que pour une partie de sa durée de travail chez l’entreprise hôte
mais ne revient pas chez l’entreprise prêteuse le reste du temps. De telles
opérations, qui impliquent un passage à temps partiel, nécessitent la
formalisation d’un avenant spéci&#64257;que au contrat de travail.</span></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>6. Le prêt peut-il être arrêté prématurément par l’une des
parties à l’opération ?</span></b></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>La modi&#64257;cation de la durée de la mise à disposition
nécessite un accord tripartite entre la société prêteuse, la société hôte et le
salarié a&#64257;n de modi&#64257;er la convention de mise à disposition et
l’avenant au contrat de travail.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Il est possible de prévoir dans la convention de mise à
disposition et dans l’avenant au contrat de travail les conditions d’une
cessation anticipée et/ou d’une prolongation de la mise à disposition. A ce
titre, des clauses facultatives sont prévues dans la convention et l’avenant
types proposés par Free-emploi.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Par ailleurs, l’entreprise prêteuse et le salarié peuvent
convenir que le prêt de main-d’œuvre est soumis à une période probatoire au
cours de laquelle il peut y être mis &#64257;n à la demande de l’une ou l’autre
des parties. Cette période probatoire est obligatoire lorsque le prêt entraine
la modi&#64257;cation d’un élément essentiel du contrat de travail (exercice de
la mission hors du secteur géographique d’origine par exemple). A ce titre, une
clause facultative est prévue dans la convention et l’avenant types proposés
par Free-emploi.</span></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>7. Une entreprise a-t-elle le droit de verser une prime
exceptionnelle à son salarié qui accepte d’être prêté ?</span></b></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'> </span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Oui, une entreprise peut tout à fait verser à son salarié
prêté une prime exceptionnelle de mobilité (qui peut être refacturée à la
société hôte) liée à la réalisation de la mise à disposition.</span></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>8. Pendant la période de prêt, le lien de subordination qui
lie l’entreprise prêteuse et le salarié disparaît-il ?</span></b></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Non, le contrat de travail entre l’entreprise prêteuse et le
salarié n’étant ni suspendu ni rompu, ce dernier continue de relever du pouvoir
hiérarchique de l’entreprise prêteuse (notamment en ce qui concerne le pouvoir
disciplinaire).</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Néanmoins, pendant la mise à disposition, le salarié est
placé sous la responsabilité fonctionnelle d’une personne de l’entreprise hôte,
qui supervisera ses activités et qu’il tiendra informée de l’exécution de sa
mission.</span></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>9. Qui est responsable des conditions de travail, de santé et
de sécurité du salarié pendant sa mise à disposition ?</span></b></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Pendant la durée de la mise à disposition, la société hôte
est responsable des conditions d’exécution du travail concernant la durée du
travail, le travail de nuit, le repos hebdomadaire, les jours fériés, la santé
et la sécurité.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Par ailleurs, il appartient à l’entreprise hôte de mettre à
disposition du salarié tous les moyens nécessaires au bon exercice de sa
mission.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>L ’entreprise hôte doit également faire béné&#64257;cier le
salarié prêté, dans les mêmes conditions que son personnel, des moyens de
transport collectifs et des installations collectives, notamment de
restauration, dont peut béné&#64257;cier ce personnel.</span></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>10. Que se passe-t-il en cas d’accident du travail au cours
de la mise à disposition ?</span></b></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Le salarié prêté victime d’un accident du travail doit
obligatoirement en informer dans les meilleurs délais l’entreprise hôte au plus
tard dans les 24 heures suivant la survenance de l’accident, par lettre
recommandée avec demande d’avis de réception. Il doit également, dans les mêmes
formes et délais, en informer son employeur.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>L ’entreprise hôte doit également informer l’entreprise
prêteuse de l’accident dont elle a eu connaissance et dont a été victime le
salarié mis à sa disposition dans un délai de 24 heures, également par lettre
recommandée.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Il revient à l’entreprise prêteuse, en tant qu’employeur du salarié
mis à disposition, d’effectuer ensuite la déclaration de l’accident auprès de
la Caisse Primaire d’Assurance Maladie dans les 48 heures suivant sa
connaissance de l’accident.</span></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>11. Quel est l’impact de la mise à disposition du salarié sur
son compte personnel de prévention de la pénibilité ?</span></b></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Avant la mise à disposition, l’entreprise prêteuse s’informe
auprès de l’entreprise hôte sur les éventuels risques professionnels existant
au sein de cette dernière et spécialement sur ceux auxquels pourrait être exposé
le salarié prêté.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>S’il apparaît que le salarié prêté sera exposé à des facteurs
de risques professionnels au-delà des seuils d’exposition &#64257;xés par
décret, l’entreprise prêteuse devra déclarer cette exposition dans le cadre de
la déclaration annuelle des données sociales (DADS) de telle sorte que le
compte personnel de prévention de la pénibilité du salarié prêté soit alimenté
en conséquence.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Si l’exposition du salarié à des facteurs de risques
professionnels au-delà des seuils d’exposition &#64257;xés par décret débute au
cours de la mise à disposition, l’entreprise hôte, qui est responsable des
conditions de santé et de sécurité, prendra les mesures de protection
nécessaires et en informera immédiatement l’entreprise prêteuse a&#64257;n que
celle-ci procède à la déclaration dans le cadre de la DADS.</span></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>12. La mise à disposition a-t-elle un impact pour le salarié
prêté en termes de mutuelle santé, retraite, prévoyance, RTT et droit à la
formation ?</span></b></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Non. Pendant toute la durée de la mise à disposition, le
contrat de travail qui lie le salarié à son employeur n’est ni rompu ni
suspendu. Le salarié continue d’appartenir au personnel de son employeur et à
béné&#64257;cier de l’ensemble des dispositions conventionnelles qui
s’appliquaient à lui avant sa mise à disposition.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>En conséquence, le salarié continue d’être af&#64257;lié aux
mêmes caisses de retraite et de prévoyance et à participer aux programmes
d’épargne collective. De même, il continue à béné&#64257;cier de RTT et des
programmes de formation professionnelle éventuellement applicable au sein de
l’entreprise prêteuse.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Si des formations spéci&#64257;ques sont nécessaires à la
mission réalisée au sein de l’entreprise hôte, cette dernière les mettra en
œuvre et en assurera le &#64257;nancement.</span></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>13. Le salarié prêté est-il soumis à un devoir de
con&#64257;dentialité</span></b><span style='font-size:14.0pt;line-height:107%'>
?</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Oui. Si le salarié prêté a accès à des informations
con&#64257;dentielles et a&#64257;n de préserver les intérêts de chacune des
parties à l’opération de mise à disposition, une clause de
con&#64257;dentialité peut &#64257;gurer dans l’avenant au contrat de travail
du salarié prêté. Cette clause, facultative, est prévue dans l’avenant type
proposé par Free-emploi  et couvre tant les informations con&#64257;dentielles
de l’entreprise prêteuse que celles de l’entreprise hôte.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Par ailleurs, a&#64257;n de préserver au mieux la
con&#64257;dentialité des informations et a&#64257;n de renforcer l’engagement
pris par le salarié, l’entreprise prêteuse et l’entreprise hôte peuvent
également s’engager l’une envers l’autre à respecter le caractère
con&#64257;dentiel des informations auxquelles le salarié a accès. Cette clause,
facultative, est prévue dans la convention type proposée par Free-emploi.</span></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>14. Le salarié prêté peut-il être débauché par l’entreprise
hôte ?</span></b></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Non, dans la mesure où une clause de non débauchage
&#64257;gure dans la convention de mise à disposition pour garantir les
intérêts de l’entreprise prêteuse. Dès lors, le salarié ne peut être embauché
par l’entreprise hôte que si l’ensemble des parties à l’opération de mise à
disposition donnent leur accord et selon les modalités à déterminer. Cette
clause, facultative, est prévue dans la convention type proposée par
Free-emploi</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>A l’issue du prêt, le salarié retrouve donc son emploi dans
l’entreprise prêteuse.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>&nbsp;</span></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>15. Quel montant l’entreprise prêteuse peut-elle refacturer à
l’entreprise hôte ?</span></b></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>La loi est très claire, le prêt de main-d’œuvre ne doit pas
avoir de but lucratif. Selon l’article L.8241-1 du Code du travail, une
opération de prêt de main-d’œuvre ne poursuit pas de but lucratif lorsque
l’entreprise prêteuse ne facture à l’entreprise hôte, pendant la mise à disposition,
que les salaires versés au salarié, les charges sociales afférentes et les
frais professionnels remboursés à l’intéressé au titre de la mise à
disposition.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>La refacturation doit donc se faire au réel.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Une prime exceptionnelle qui serait versée au salarié pendant
sa mise à disposition et liée à la mise à disposition peut faire l’objet d’une
refacturation comme les autres salaires.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>En revanche, l’entreprise prêteuse ne peut pas refacturer à
l’entreprise hôte des montants relatifs à la participation et à l’intéressement.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>En&#64257;n, la jurisprudence a tendance à considérer que la
facturation de « frais de gestion » par l’entreprise prêteuse tend à
caractériser le but lucratif de l’opération et entraîne la
requali&#64257;cation en prêt de main-d’œuvre illicite. L ’administration
&#64257;scale s’est quant à elle prononcée en faveur d’une facturation des
frais de gestion sous réserve qu’ils demeurent modérés et justi&#64257;és. Si
l’entreprise prêteuse décide de refacturer une partie des frais de gestion,
ceux-ci doivent être précisément chiffrables, justi&#64257;ables et
individualisables, c’est à dire correspondre à un coût réel.</span></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>16. Comment est comptabilisé le prêt de salarié dans les
comptes de la société prêteuse et dans ceux de la société hôte ?</span></b></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>L ’entreprise prêteuse enregistre la facture qu’elle émet
dans les comptes « 708400 Mise à disposition de personnel facturée » et «
708500 Ports et frais accessoires facturés ».</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>L’entreprise hôte enregistre la facture qu’elle reçoit dans
le compte « 621 Personnel extérieur à l’entreprise ».</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Les opérations de mise à disposition sont assujetties à la
TVA. Par exception, des exonérations de TVA sont envisageables au cas par cas
en fonction de la nature juridique des entités concernées et de l’objectif
poursuivi par cette mise à disposition (exercice d’une activité rentrant dans
le champ d’application de la TVA ou non).</span></p>

