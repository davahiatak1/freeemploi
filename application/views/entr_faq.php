<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>17. Le salarié mis à disposition doit-il être comptabilisé
dans les effectifs de l’entreprise hôte ?</span></b></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Dans tous les cas, le salarié continue d’appartenir au personnel
de son employeur.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Il doit simultanément être comptabilisé dans les effectifs de
l’entreprise hôte s’il est présent dans les locaux de cette dernière et s’il y
travaille depuis au moins un an, ces deux conditions étant cumulatives.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>La période d’un an ne s’entend pas nécessairement comme une
période de 12 mois continue : si un salarié mis à disposition était déjà
présent dans l’entreprise hôte un an avant la date de l’événement qui implique
un calcul des effectifs, ce salarié est décompté dans l’effectif de
l’entreprise hôte à due proportion de son temps de présence au cours des 12
mois précédents.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Par ailleurs, un salarié mis à disposition ne peut être
électeur aux élections des délégués du personnel et/ou du comité d’entreprise
de l’entreprise hôte que s’il justi&#64257;e de 12 mois continus de présence
dans l’entreprise hôte.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>En&#64257;n, pour être éligible aux élections des délégués du
personnel, la condition de présence est prorogée à 24 mois continus au sein de
l’entreprise hôte (le salarié ne peut pas se présenter en qualité de candidat
aux élections du comité d’entreprise).</span></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>18. Un salarié peut-il faire l’objet de plusieurs mises à
disposition en même temps ?</span></b></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Oui, un salarié peut faire l’objet de plusieurs mises à
disposition en même temps.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>De façon pratique, cela requiert des documents similaires à
une mise à disposition simple. Free-emploi est équipé, et à vos côtés, pour
vous accompagner dans la mise en place d'un schéma de ce type si tel est votre
besoin.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Vous devrez veiller à ce que la somme des temps de travail du
salarié mis à disposition n’excède pas celle inscrite dans son contrat de
travail. A moins bien sûr que vous n’ayez son accord exprès pour modi&#64257;er
cet élément essentiel de son contrat pour la durée des mises à disposition.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'> </span></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>19. La législation applicable au temps partiel sur la durée
minimum du temps de travail de 24 heures par semaine est-elle applicable au
prêt de main-d’œuvre ?</span></b></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Non, la durée minimale de travail hebdomadaire de 24 heures
n’est applicable qu’aux salariés titulaires d’un contrat de travail à temps
partiel. Cette limite n’est pas applicable aux salariés titulaires d’un contrat
de travail à temps plein, pas plus qu’à ceux qui feraient l’objet d’une mise à
disposition. En conséquence, un salarié titulaire d’un contrat de travail à
temps plein peut faire l’objet d’une mise à disposition pour une durée
inférieure à 24 heures par semaine, dès lors qu’il continue de travailler chez
son employeur le reste du temps.</span></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>20. Si une clause du contrat de travail risque d’être violée
du fait de la mise à disposition, l’avenant doit-il spéci&#64257;quement faire
référence à cette clause pour la suspendre temporairement ?</span></b></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Oui, si une clause du contrat de travail du salarié prêté,
telle une clause d’exclusivité, risque d’être violée du seul fait ou au cours
de la mise à disposition, il convient d’y faire référence dans l’avenant au
contrat de travail a&#64257;n d’en suspendre temporairement l’application. A
défaut, l’entreprise prêteuse qui avait accepté d’y déroger sans l’encadrer
pourrait dif&#64257;cilement s’en prévaloir par la suite.</span></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>21. Le prêt de salariés est-il réservé uniquement aux
sociétés ?</span></b></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Non, certaines personnes morales autres que des sociétés
peuvent également recourir au prêt de main-d’œuvre.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Toutes les sociétés dont les salariés sont employés dans les
conditions de droit privé, quelle que soit leur forme juridique, peuvent avoir
recours au prêt de compétences. Ainsi, les sociétés d’économie mixte, qui sont
des sociétés anonymes soumises, dans les relations avec leurs salariés, aux
dispositions du Code du travail, peuvent prêter et emprunter des salariés
conformément aux articles L.8241-1 et suivants du Code du travail.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>De même, les établissements publics à caractère industriel et
commercial (EPIC) et les associations, qui sont des employeurs de droit privé
soumis eux aussi, dans les relations avec leurs salariés, aux dispositions du
Code du travail, peuvent également prêter et emprunter des salariés
conformément aux articles L.8241-1 et suivants du Code du travail.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>En revanche, la mise à disposition du personnel de l’une des
trois fonctions publiques (de l’Etat, territoriale ou hospitalière) auprès d’un
organisme tiers (mise à disposition dite « sortante ») ainsi que l’accueil, par
un établissement appartenant à l’une des trois fonctions publiques, de
personnels de droit privé (mise à disposition dite « entrante »), sont encadrés
par des lois et décrets spéci&#64257;ques. Les modèles de convention et d’avenant
proposés par Free-emploi  sont prévus pour encadrer des mises à disposition
régies par les articles L.8241-1 et suivants du Code du travail. Ils ne sont
donc pas adaptés pour des mises à disposition impliquant des établissements
appartenant à l’une des trois fonctions publiques.</span></p>

