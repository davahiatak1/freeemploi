<p class=MsoNormal><b>                                            
INFORMATIONS  PRATIQUES /DES REPONSES A VOS QUESTIONS</b></p>

<p class=MsoNormal>Questions fréquentes des salariés</p>

<p class=MsoNormal><b><span style='font-size:14.0pt;line-height:107%'>1.
Qu’est-ce que l’employabilité ?</span></b></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Selon l’Organisation Internationale du Travail (OIT),
l’employabilité est « l’aptitude de chacun à trouver et conserver un emploi, à
progresser au travail et à s’adapter au changement tout au long de la vie
professionnelle ».</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Le prêt de compétences est un moyen très efficace pour les
salariés de développer leur employabilité.</span></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>2. Le prêt de compétences est-il légal ?</span></b></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Oui, le prêt de main-d’œuvre (ou « prêt de compétences » / «
prêt de salariés ») est tout à fait légal. Il est encadré par le  Code du
travail qui précisent notamment que l’accord du salarié est obligatoire.</span></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>3. La durée de la mise à disposition doit-elle obéir à
certaines règles ?</span></b></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Non, le prêt de compétences est très souple dans son
application, notamment pour ce qui est de sa durée. Ainsi, le prêt peut être
d’un jour ou de plusieurs mois, pour tout ou partie seulement du temps de travail
du salarié. La durée du prêt doit néanmoins être déterminée.</span></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>4. Un salarié peut-il être mis à disposition d’une entreprise
ne relevant pas de la même convention collective que son employeur ?</span></b></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Oui, cela ne pose aucun problème. La convention collective
applicable au salarié demeurera celle en vigueur au sein de son employeur,
l’entreprise prêteuse.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>En conséquence, le salarié conserve, pendant la période de
mise à disposition, le béné&#64257;ce de l’ensemble des avantages de ladite
convention collective et plus généralement de l’ensemble des avantages
applicables au sein de l’entreprise prêteuse.</span></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>5. Le salarié mis à disposition doit-il signer un contrat de
travail avec l’entreprise hôte ?</span></b></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Non, le salarié ne doit signer qu’un avenant au contrat de
travail avec son employeur, l’entreprise prêteuse. Son contrat de travail est
donc maintenu et il n’a aucun document juridique à signer avec l’entreprise
hôte.</span></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>6. Une fois l’avenant signé, le salarié est-il obligé
d’accepter toutes les propositions de prêt de son employeur ?</span></b></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Non, en aucun cas. Chaque mise à disposition doit donner lieu
à la signature d’un avenant au contrat de travail (l’avenant peut d’ailleurs
être différent d’une mise à disposition à l’autre). L’accord du salarié est
donc requis pour chaque mise à disposition.</span></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>7. Un salarié ayant refusé d’être mis à disposition d’une
autre entreprise peut-il être sanctionné ?</span></b></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Non, la loi est formelle, un salarié ne peut être sanctionné,
licencié ou faire l’objet d’une mesure discriminatoire pour avoir refusé une
proposition de mise à disposition.</span></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>8. Si le salarié engage des frais dans le cadre de sa mise à
disposition, ceux-ci lui sont-ils remboursés</span></b><span style='font-size:
14.0pt;line-height:107%'> ?</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Oui, les frais de mission, notamment les frais de
déplacements, engagés par le salarié dans le cadre de sa mise à disposition lui
sont remboursés par l’entreprise prêteuse selon les modalités en vigueur au
sein de cette dernière.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'> </span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>9. <b>Pendant la période de prêt, le lien de subordination
qui lie l’entreprise prêteuse et le salarié disparaît-il ?</b></span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Non, le contrat de travail entre l’entreprise prêteuse et le
salarié n’étant ni suspendu ni rompu, ce dernier continue de relever du pouvoir
hiérarchique de l’entreprise prêteuse (notamment en ce qui concerne le pouvoir
disciplinaire).</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Néanmoins, pendant la mise à disposition, le salarié est
placé sous la responsabilité fonctionnelle d’une personne de l’entreprise hôte,
qui supervisera ses activités et qu’il tiendra informée de l’exécution de sa
mission.</span></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>10. Qui est responsable des conditions de travail, de santé
et de sécurité du salarié pendant sa mise à disposition ?</span></b></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Pendant la durée de la mise à disposition, la société hôte
est responsable des conditions d’exécution du travail concernant la durée du
travail, le travail de nuit, le repos hebdomadaire, les jours fériés, la santé
et la sécurité.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Par ailleurs, il appartient à l’entreprise hôte de mettre à
disposition du salarié tous les moyens nécessaires au bon exercice de sa
mission.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>L’entreprise hôte doit également faire béné&#64257;cier le
salarié prêté, dans les mêmes conditions que son personnel, des moyens de
transport collectifs et des installations collectives, notamment de
restauration, dont peut béné&#64257;cier ce personnel.</span></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>11. Quel est l’impact de la mise à disposition du salarié sur
son compte personnel de prévention de la pénibilité ?</span></b></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Avant la mise à disposition, l’entreprise prêteuse s’informe
auprès de l’entreprise hôte sur les éventuels risques professionnels existant
au sein de cette dernière et spécialement sur ceux auxquels pourrait être
exposé le salarié prêté.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>S’il apparaît que le salarié prêté sera exposé à des facteurs
de risques professionnels au-delà des seuils d’exposition &#64257;xés par
décret, l’entreprise prêteuse devra déclarer cette exposition dans le cadre de
la déclaration annuelle des données sociales (DADS) de telle sorte que le
compte personnel de prévention de la pénibilité du salarié prêté soit alimenté
en conséquence.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'> Si l’exposition du salarié à des facteurs de risques
professionnels au-delà des seuils d’exposition &#64257;xés par décret débute au
cours de la mise à disposition, l’entreprise hôte, qui est responsable des
conditions de santé et de sécurité, prendra les mesures de protection
nécessaires et en informera immédiatement l’entreprise prêteuse a&#64257;n que
celle-ci procède à la déclaration dans le cadre de la DADS.</span></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>12. La mise à disposition a-t-elle un impact pour le salarié
prêté en termes de mutuelle santé, retraite, prévoyance, RTT et droit à la
formation ?</span></b></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Non. Pendant toute la durée de la mise à disposition, le
contrat de travail qui lie le salarié à son employeur n’est ni rompu ni
suspendu. Le salarié continue d’appartenir au personnel de son employeur et à
béné&#64257;cier de l’ensemble des dispositions conventionnelles qui
s’appliquaient à lui avant sa mise à disposition.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>En conséquence, le salarié continue d’être af&#64257;lié aux
mêmes caisses de retraite et de prévoyance et à participer aux programmes
d’épargne collective. De même, il continue à béné&#64257;cier de RTT et des
programmes de formation professionnelle éventuellement applicables au sein de
l’entreprise prêteuse.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Si des formations spéci&#64257;ques sont nécessaires à la
mission réalisée au sein de l’entreprise hôte, cette dernière les mettra en
œuvre et en assurera le &#64257;nancement.</span></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>13. Le salarié peut-il prendre des vacances (congés ou RTT)
pendant le prêt ?</span></b></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Oui, le salarié peut bien entendu prendre des vacances
pendant sa période de mise à disposition. Comme il continue de relever du
pouvoir hiérarchique de l’entreprise prêteuse, c’est toujours cette dernière
qui valide les congés / RTT demandés par le salarié.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Pour des raisons pratiques et dans un souci de bonne
réalisation de la mission de mise à disposition, il convient bien entendu d’en
avertir le plus tôt possible l’entreprise hôte.</span></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>14. Que doit faire un salarié s’il tombe malade pendant le
prêt ?</span></b></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'> </span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>En cours de prêt, le salarié malade est soumis aux mêmes
obligations de déclaration à son employeur et à la sécurité sociale. Ainsi, en
cas d’arrêt maladie, le salarié doit en avertir son employeur et la caisse
primaire d’assurance maladie dans les 48 heures.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Pour des raisons pratiques et dans un souci de bonne
réalisation de la mission de mise à disposition, il convient également d’en
avertir l’entreprise hôte dans les mêmes conditions.</span></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>15. Le salarié peut-il arrêter sa mission chez l’entreprise
hôte en cours d’exécution ?</span></b></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>La modi&#64257;cation de la durée de la mise à disposition
nécessite un accord tripartite entre le salarié, la société prêteuse et la
société hôte a&#64257;n de modi&#64257;er la convention de mise à disposition
(signée entre l’entreprise prêteuse et l’entreprise hôte) et l’avenant au contrat
de travail.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Il est possible de prévoir dans la convention de mise à
disposition et dans l’avenant au contrat de travail les conditions d’une
cessation anticipée et/ou d’une prolongation de la mise à disposition. A ce
titre, des clauses facultatives sont prévues dans la convention et l’avenant
types proposés par Free-emploi.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Par ailleurs, l’entreprise prêteuse et le salarié peuvent
convenir que le prêt de main-d’œuvre est soumis à une période probatoire au
cours de laquelle il peut y être mis &#64257;n à la demande de l’une ou l’autre
des parties. Cette période probatoire est obligatoire lorsque le prêt entraine
la modi&#64257;cation d’un élément essentiel du contrat de travail (exercice de
la mission hors du secteur géographique d’origine par exemple). A ce titre, une
clause facultative est prévue dans la convention et l’avenant types proposés
par Free-emploi</span></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>16. Que se passe-t-il à l’issue du prêt ?</span></b></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>A l’issue de la mise à disposition, le salarié retrouve son
emploi dans l’entreprise prêteuse sans que l’évolution de sa carrière ou de sa
rémunération ne soit affectée par la période de prêt.</span></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>17. Un salarié peut-il se faire embaucher à l’issue du prêt
par l’entreprise hôte ?</span></b></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Oui, dès lors que l’ensemble des parties à l’opération de
mise à disposition donnent leur accord et selon les modalités à déterminer.</span></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>18. Que doit faire un salarié intéressé par le prêt de
compétences ?</span></b></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Un salarié qui souhaite béné&#64257;cier des avantages du
prêt de compétences peut en parler à sa hiérarchie ou nous contacter a&#64257;n
d’obtenir plus d’informations ou a&#64257;n que Free-emploi prenne directement
contact avec son employeur.</span></p>

