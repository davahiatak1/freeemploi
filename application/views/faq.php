<p class=MsoNormal><b><span style='font-size:14.0pt;line-height:107%'>                                                   
Questions utiles</span></b></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>1. Qu’est-ce que le prêt de salariés (ou prêt de maind’œuvre)
?</span></b></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Le prêt de salariés (ou prêt de main-d’œuvre) est une
opération par laquelle une société met temporairement les compétences d’un de
ses salariés (avec l’accord de celui-ci) à la disposition d’une autre société.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>La société prêteuse facture à la société hôte les salaires
versés au salarié pendant le prêt, les charges sociales afférentes et les
éventuels frais professionnels remboursés au salarié au titre de la mise à
disposition.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>2<b>. Pourquoi passer par Free-emploi pour prêter / emprunter
un salarié ?</b></span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Free-emploi  est une plateforme d’information et de mise en
relation de sociétés désireuses de se prêter des salariés. Jusqu’à présent,
seules les sociétés qui se connaissaient pouvaient mettre en place cette
solution, souvent avec des incertitudes ou des dif&#64257;cultés juridiques.
Grâce à Free-emploi, les sociétés de toutes tailles, de tous secteurs et de
toutes régions peuvent maintenant se rencontrer a&#64257;n d’optimiser la
gestion de leurs ressources humaines.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Free-emploi facilite :</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>la rencontre des sociétés prêteuses et des sociétés hôtes
grâce à son algorithme de matching en faisant coïncider les « compétences
disponibles » d’une part avec les «&nbsp;besoins exprimés&nbsp;» d’autre part ;
la réalisation du prêt de main-d’œuvre en fournissant notamment la
documentation juridique nécessaire (convention de mise à disposition et avenant
au contrat de travail types) . Le prêt de main-d’œuvre est légal mais
juridiquement très encadré. Passer par Free-emploi pour prêter / emprunter un
salarié avec l’accord de ce dernier, c’est la garantie de réaliser l’opération
facilement et dans un cadre juridique sécurisé.</span></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>3. Le prêt de salariés est-il légal ?</span></b></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Oui, le prêt de salarié est tout à fait légal.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>L ’article L.8241-2 du Code du travail précise en effet que «
les opérations de prêt de main-d’œuvre à but non lucratif sont autorisées ».
Une opération de prêt de main-d’œuvre ne poursuit pas de but lucratif lorsque
l’entreprise prêteuse ne facture à l’entreprise hôte, pendant la mise à
disposition, que les salaires versés au salarié, les charges sociales
afférentes et les frais professionnels remboursés à l’intéressé au titre de la
mise à disposition.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Cette opération suppose l’accord du salarié concerné.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>En&#64257;n, l'article L.8231-1 du Code du travail relatif au
délit de marchandage interdit toute opération à but lucratif de fourniture de
main-d'œuvre qui a pour effet de causer un préjudice au salarié qu'elle concerne
ou d'éluder l'application de dispositions légales ou de stipulations d'une
convention ou d'un accord collectif de travail.</span></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>4. Pourquoi une société a-t-elle intérêt à prêter un salarié
?</span></b></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Le prêt de salariés permet à l’entreprise prêteuse :</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>d’offrir de la mobilité sécurisée à ses salariés ; de
développer et de conserver les compétences de ses employés ; d’adapter ses
charges de personnel en fonction de son activité ; de maintenir les contrats de
travail en cas de diminution d’activité ou en période de crise ; de dynamiser
et soutenir l’économie locale et régionale.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>&nbsp;</span></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>5. Pourquoi une société a-t-elle intérêt à recourir à un
salarié d’une autre entreprise ?</span></b></p>

<p class=MsoNormal style='text-align:justify'><i><span style='font-size:14.0pt;
line-height:107%'>un cabinet d’avocat spécialisé en droit social, ainsi que les
outils de communication interne à l’attention des salariés et des Instances
Représentatives du Personnel (IRP).</span></i></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Le prêt de salariés permet à l’entreprise hôte :</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>d’accéder sans risque à de la main-d’œuvre / expertise
temporaire en payant uniquement le coût salarial sans les coûts liés à l’intervention
d'un intermédiaire habituel ; de recourir à de la main-d’œuvre motivée, bien
formée et qui s’intègre facilement ; de favoriser l’embauche du salarié dès
lors que l’ensemble des parties en sont d’accord et selon les modalités à
déterminer ; de dynamiser et soutenir l’économie locale et régionale.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>&nbsp;</span></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>6. Pour le salarié, quels sont les avantages du prêt de
main-d’œuvre ?</span></b></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Le prêt de main-d’œuvre présente de nombreux avantages pour
le salarié :</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>il développe ses compétences et diversi&#64257;e son
expérience ; il améliore son employabilité ; il conserve son contrat de travail
avec son employeur : pendant la période du prêt, son contrat de travail n’est
ni rompu ni suspendu. Le salarié continue d’appartenir au personnel de
l’entreprise prêteuse ; il conserve le béné&#64257;ce de l’ensemble des
dispositions conventionnelles dont il aurait béné&#64257;cié s’il avait exécuté
son travail dans l’entreprise prêteuse ainsi que tous les avantages applicables
au sein de cette dernière ; il maintient son salaire qui lui est toujours versé
par son employeur (l’entreprise prêteuse) ; il béné&#64257;cie en période de
crise d’une alternative lui permettant de conserver son contrat de travail.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>&nbsp;</span></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>7. En pratique, comment mettre en place le prêt de salariés ?</span></b></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>La mise en œuvre du prêt de salariés est très simple.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Une convention de mise à disposition est signée par
l’entreprise prêteuse et l’entreprise hôte. Cette convention dé&#64257;nit
notamment la durée du prêt et mentionne l’identité et la quali&#64257;cation du
salarié concerné, ainsi que le mode de détermination des salaires, des charges
sociales et des frais professionnels qui seront facturés à l’entreprise hôte
par l’entreprise prêteuse (un modèle de convention est disponible sur le site
mobiliwork.com).</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Parallèlement, le salarié signe un avenant à son contrat de
travail qui précise notamment le travail con&#64257;é dans l’entreprise hôte,
les horaires et le lieu d’exécution du travail, ainsi que les caractéristiques
particulières du poste de travail (un modèle d’avenant est disponible sur le
site mobiliwork.com).</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Le comité d’entreprise ou, à défaut, les délégués du
personnel de l’entreprise prêteuse sont consultés préalablement à la mise en
œuvre du prêt et informés des différentes conventions signées. Le comité d’hygiène,
de sécurité et des conditions de travail (CHSCT) est également informé lorsque
le poste occupé dans l’entreprise hôte par le salarié mis à disposition
&#64257;gure sur la liste de ceux présentant des risques particuliers pour la
santé ou la sécurité des salariés (mentionnée au second alinéa de l’article
L.4154-2 du Code du travail).</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Le comité d’entreprise et le CHSCT ou, à défaut, les délégués
du personnel de l’entreprise hôte sont informés et consultés préalablement à
l’accueil de salariés mis à la disposition de celle-ci dans le cadre de prêts
de main-d’œuvre.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>L’entreprise prêteuse facture à l’entreprise hôte, pendant la
mise à disposition, les salaires versés au salarié, les charges sociales
afférentes et les frais professionnels remboursés à l’intéressé au titre de la
mise à disposition. Cette facturation peut être assurée via un suivi de
présence du salarié mis à disposition au sein de l’entreprise hôte.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Les sociétés n’ont aucune déclaration à effectuer auprès de
l’URSSAF et de l’Inspection du travail.</span></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>8. L ’accord du salarié pour la mise en place du prêt est-il
obligatoire ?</span></b></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Oui, le salarié doit obligatoirement donner son accord pour
que la mise à disposition puisse avoir lieu, et signer un avenant à son contrat
de travail.</span></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>9. Le prêt de salariés est-il restreint à certaines sociétés
?</span></b></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Non, toutes les sociétés, quels que soient leur forme
juridique, leur taille et leur secteur d’activité peuvent recourir au prêt de
main-d’œuvre.</span></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>10. Une société peut-elle simultanément prêter et emprunter
des salariés ?</span></b></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Aucune disposition légale n’empêche une même société de
prêter un ou plusieurs salariés à une autre entreprise et de recourir
simultanément aux compétences d'un ou plusieurs autres salariés auprès de cette
même entreprise ou d’une troisième société.</span></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>11. Peut-on &#64257;xer librement la durée du prêt ?</span></b></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Aucune disposition légale ne limite la durée de la mise à
disposition mais elle doit être déterminée : le prêt peut être d’un jour ou de
plusieurs mois, à temps plein ou seulement d’un ou plusieurs jours par semaine.</span></p>

<p class=MsoNormal style='text-align:justify'><b><span style='font-size:14.0pt;
line-height:107%'>12. Dans quelles conditions peut-il être mis &#64257;n
prématurément au prêt ?</span></b></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>La modi&#64257;cation de la durée de la mise à disposition
nécessite un accord tripartite entre la société prêteuse, la société hôte et le
salarié a&#64257;n de modi&#64257;er la convention de mise à disposition et
l’avenant au contrat de travail.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Il est possible de prévoir dans la convention de mise à
disposition et dans l’avenant au contrat de travail les conditions d’une
cessation anticipée et/ou d’une prolongation de la mise à disposition.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Par ailleurs, l’entreprise prêteuse et le salarié peuvent
convenir que le prêt de main-d’œuvre est soumis à une période probatoire au
cours de laquelle il peut y être mis &#64257;n à la demande de l’une ou l’autre
des parties. Cette période probatoire est obligatoire lorsque le prêt de
main-d‘œuvre entraine la modi&#64257;cation d’un élément essentiel du contrat
de travail.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>13. <b>Que se passe-t-il à l’issue du prêt ?</b></span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>A l’issue de la mise à disposition, le salarié retrouve son
emploi dans l’entreprise prêteuse sans que l’évolution de sa carrière ou de sa
rémunération ne soit affectée par la période de prêt.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>14<b>. À l’issue du prêt, le salarié peut-il être embauché
par la société hôte ?</b></span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Non, dans la mesure où une clause de non débauchage
&#64257;gure dans la convention de mise à disposition pour garantir les
intérêts de l’entreprise prêteuse. Dès lors, le salarié ne peut être embauché
par l’entreprise hôte que si l’ensemble des parties à l’opération de mise à
disposition donnent leur accord et selon les modalités à déterminer. Cette
clause, facultative, est prévue dans la convention type proposée par
Free-emploi.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>A l’issue du prêt, le salarié retrouve donc son emploi dans
l’entreprise prêteuse.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>15<b>. Au cours du prêt, quel est le partage des rôles entre
la société hôte et la société prêteuse ?</b></span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Le contrat de travail du salarié mis à disposition n’étant
pas suspendu, celui-ci continue de relever du pouvoir hiérarchique de
l’entreprise prêteuse.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Néanmoins, pendant la durée de la mise à disposition, la
société hôte est responsable des conditions d’exécution du travail concernant
la durée du travail, le travail de nuit, le repos hebdomadaire, les jours
fériés, l’hygiène et la sécurité.</span></p>

<p class=MsoNormal style='text-align:justify'><span style='font-size:14.0pt;
line-height:107%'>Il appartient à l’entreprise hôte de mettre à disposition du
salarié tous les moyens nécessaires au bon exercice de sa mission.</span></p>

