        var wrapper = $('.field_wrapper'); //Input field wrapper
        var fieldHTML = '<div class="col-xs-12 input-group">\n\
            <a style="float:right; float:top;" href="javascript:void(0);" class="remove_button" title="Remove field"><span class="glyphicon glyphicon-minus"></span></a>\n\
<input type="text" name="question[]"  class="form-control" placeholder="Votre question"/>\n\
</div>'; //New input field html 
        var x = 1; //Initial field counter is 1
        $(addButton).click(function () { //Once add button is clicked
            if (x < maxField) { //Check maximum number of input fields
                x++; //Increment field counter
                $(wrapper).append(fieldHTML); // Add field html
            }
        });
        $(wrapper).on('click', '.remove_button', function (e) { //Once remove button is clicked
            e.preventDefault();
            $(this).parent('div').remove(); //Remove field html
            x--; //Decrement field counter
        });
    });
</script>

